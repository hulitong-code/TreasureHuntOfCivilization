import type { Subject } from '@shared/types';
import { getLanguage } from '../i18n';

// 双语问题接口
export interface BilingualQuestion {
  id: string;
  subject: Subject;
  difficulty: 1 | 2 | 3;
  zh: {
    question: string;
    options: string[];
    explanation: string;
    knowledgePoint: string;
  };
  en: {
    question: string;
    options: string[];
    explanation: string;
    knowledgePoint: string;
  };
  correctIndex: number;
  relatedCivilization?: string;
}

// 获取当前语言的问题
export function getLocalizedQuestion(q: BilingualQuestion) {
  const lang = getLanguage();
  const localized = q[lang];
  return {
    id: q.id,
    subject: q.subject,
    difficulty: q.difficulty,
    question: localized.question,
    options: localized.options,
    correctIndex: q.correctIndex,
    explanation: localized.explanation,
    knowledgePoint: localized.knowledgePoint,
    relatedCivilization: q.relatedCivilization,
  };
}

